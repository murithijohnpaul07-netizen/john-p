#include <stdio.h>
int main() {
    int i;

    printf("Numbers from 100 down to 50 in descending order:\n");

    // for loop counting down from 100 to 50
    for (i = 100; i >= 50; i--) 
    {
        printf("%d\n", i);
    }

    return 0;
}